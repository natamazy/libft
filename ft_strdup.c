#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*res;
	int		i;

	res = (char *) malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!res)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
